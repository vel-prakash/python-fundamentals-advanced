# Custom Module Example
# This module contains reusable constants and functions
# that can be imported into other Python files.

pi = 3.14159

def square(x):
    """Return the square of a number."""
    return x ** 2

def cube(x):
    """Return the cube of a number."""
    return x ** 3

def circumference(radius):
    """Calculate the circumference of a circle."""
    return 2 * pi * radius

def area(radius):
    """Calculate the area of a circle."""
    return pi * radius ** 2