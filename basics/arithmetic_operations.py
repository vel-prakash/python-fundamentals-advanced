# -------- Arithmetic Operations --------
# This file demonstrates two methods:
# 1. Standard operations (a = a + 1)
# 2. Shorthand operations (a += 1)

# -------- Initial Value --------
friends = 0

# -------- Addition --------
friends = friends + 1
friends += 1

# -------- Subtraction --------
friends = friends - 0.5
friends -= 1

# -------- Multiplication --------
friends = friends * 3
friends *= 3

# -------- Division --------
friends = friends /2
friends /= 2

# -------- More Operations --------
friends += 0.875
friends = friends **2
friends **= 2
 
print(friends)

# -------- Modulus --------
remainder = friends % 3

print(remainder)