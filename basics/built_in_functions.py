# -------- Variables --------
x = 3.14
y = -4
z = 5

# -------- Built-in Functions --------
round_result = round(x)
abs_result = abs(y)
pow_result = pow(x, 3)
max_result = max(x, y, z)
min_result = min(x, y, z)

# -------- Output --------
print(round_result)
print(abs_result)
print(pow_result)
print(max_result)
print(min_result)